            <p class="mb-1"><b>O Parque Natural Municipal de Castanhal</b>, também conhecido como antigo Horto Florestal, regulamentado por meio da Lei Municipal nº 020/2018, de 30 de maio de 2018. É uma Unidade de Conservação Natural com aproximadamente 15,225 hectares, localizado no bairro Cariri do município de Castanhal, entre a rua Padre Salvador Tracaioli, passagens Arame e São João.
            </p>
            <p class="mb-1">
                A criação do Parque Natural Municipal de Castanhal tem por objetivos:
            </p>
            <ul class="list-unstyled mb-1">
                <li><i class="fa-solid fa-check text-pmc-2"></i> Preservação de nascentes, para que seja assegurado as condições de existência e reprodução de espécies da flora e da fauna residente e migratório;</li>
                <li><i class="fa-solid fa-check text-pmc-2"></i> Conservação e proteção da castanheira-do-pará Bertholletia Excelsa H.&.B, da samaúma Ceiba pentandra e do pau brasil Paubrasilia echinata Lam;</li>
                <li><i class="fa-solid fa-check text-pmc-2"></i> Contribuir para melhoria do microclima de Castanhal;</li>
                <li><i class="fa-solid fa-check text-pmc-2"></i> Assegurar acesso a estudos e pesquisas visando propor ações que diminua o risco de extinção local das espécies ameaçadas;</li>
                <li><i class="fa-solid fa-check text-pmc-2"></i> Garantir a proteção dos recursos naturais englobados e socioculturais existentes no entorno do parque;</li>
                <li><i class="fa-solid fa-check text-pmc-2"></i> Assegurar a integridade dos seus atributos e manutenção do equilíbrio ecológico existente, quando da realização de atividades permitidas;</li>
                <li><i class="fa-solid fa-check text-pmc-2"></i> Assegurar o desenvolvimento de projetos alternativos que proporcionem renda às famílias, assim como ações de educação ambiental como forma de diminuição da pressão humana sobre a biodiversidade do local;</li>
                <li><i class="fa-solid fa-check text-pmc-2"></i> Promover o desenvolvimento sustentável das populações que habitam o entorno do Parque, com prioridade para o combate à pobreza e melhoria das suas condições de vida.</li>
            </ul>
            <p class="text-danger p-4 border border-danger">Observação: Para obter mais informações a respeito, confira na Lei Municipal nº 020/2018, de 30 de maio de 2018, ou faça uma visita à Secretaria Municipal de Meio Ambiente de Castanhal.</p>
