            <p class="mb-0"><b>O Secretário Municipal de Meio Ambiente</b>, compete:</p>
            <ul class="list-unstyled">
                <li class="mt-1">I  - 	Representar e defender os interesses do Município no que referir as finalidades e objetivos da Secretaria Municipal de Meio Ambiente, em consonância com a legislação Federal, Estadual e Municipal.</li>       
            </ul>
            
            <p class="mb-0"><b>À Assessoria Técnica e de Estudos legislativos</b>, compete:</p>
            <ul class="list-unstyled">
                <li class="mt-1">I - 	Examinar processos e expedientes submetidos à apreciação do Secretário; </li>       
                <li class="mt-1">II - 	Elaborar respostas e pareceres técnicos às solicitações vinculadas à Secretaria; </li>       
                <li class="mt-1">III - 	Prestar apoio técnico às Coordenadorias e demais Unidades Administrativas do Secretaria em assuntos determinados pelo Secretário; </li>       
                <li class="mt-1">IV - 	Examinar e opinar sobre atos normativos, editais, convênios, outros ajustes e demais instrumentos legais de responsabilidade e interveniência da Secretaria; </li>       
                <li class="mt-1">V - 	Prestar assessoria na elaborar estudos legislativos; </li>       
                <li class="mt-1">VI - 	Analisar e/ou elaborar minutas de normas para subsidiar a atuação da Secretaria e do Conselho Municipal de Defesa do Meio Ambiente.</li>             
            </ul>
            
            <p class="mb-0"><b>À Assessoria de Tecnologia da Informação e de Geotecnologia</b>, compete:</p>
            <ul class="list-unstyled">
                <li class="mt-1">I - 	Coordenar os programas, projetos e ações de caráter permanente na área de Tecnologia da Informação e seus sistemas corporativos;</li>       
                <li class="mt-1">II - 	Coordenar os programas, projetos e ações de caráter permanente na área de Geotecnologia.</li>       
            </ul>
            
            <p class="mb-0"><b>À Coordenadoria de Gestão Administrativa e Financeira</b>, compete:</p>
            <ul class="list-unstyled">
                <li class="mt-1">I - 	Coordenar, executar, controlar e supervisionar as atividades de execução orçamentária, de administração financeira e contábil, e de registro e controle de receitas, de gestão e desenvolvimento dos recursos humanos, de serviços gerais, de infraestrutura, logística, material e patrimônio e de documentos e arquivos; </li>       
                <li class="mt-1">II - 	Cumprir as diretrizes gerais administrativas estabelecidas no município e acompanhar a execução da política de administração e finanças, observadas as normas estabelecidas pela legislação pertinente.</li>       
            </ul>
            
            <p class="mb-0"><b>À Coordenadoria de Fiscalização Ambiental</b>, compete:</p>
            <ul class="list-unstyled">
                <li class="mt-1">I - 	Planejar, coordenar, acompanhar, avaliar e supervisionar as ações de fiscalização em empreendimentos e atividades efetiva ou potencialmente poluidoras e/ou degradadoras do meio ambiental e do uso ou exploração dos recursos naturais; </li>       
                <li class="mt-1">II - 	Combater os crimes contra fauna, de forma articulada com Organizações Públicas integrantes do Sistema Municipal de Meio Ambiente e Sociedade Civil Organizada, em conformidade com a legislação ambiental em vigor; </li>       
                <li class="mt-1">III - 	Fiscalizar o cumprimento das condicionantes, exigências e restrições estabelecidas no licenciamento ambiental, bem como das obrigações ambientais impostas através de Termos de Ajustamento de Condutas, Planos de Recuperação de Áreas Degradadas, dentre outros.</li>       
            </ul>
            
            <p class="mb-0"><b>À Coordenadoria de Licenciamento Ambiental</b>, compete:</p>
            <ul class="list-unstyled">
                <li class="mt-1">I - 	Planejar, coordenar, executar e orientar o licenciamento ambiental e os demais atos autorizativos de empreendimentos ou atividades industriais, minerárias, de obras civis, de infraestrutura urbanísticas, de saneamento, de comércio, serviços, aproveitamento e disposição de resíduos, de atividades agrossilvipastoris, fauna, aquicultura, pesca e demais atividades consideradas efetiva ou potencialmente poluidoras e/ou degradadoras do meio ambiente; </li>       
                <li class="mt-1">II - 	Coordenar as atividades de apoio ao ordenamento ambiental visando à regularização das propriedades rurais e prevenção e combate ao desmatamento; </li>       
                <li class="mt-1">III - 	Apoiar a pesquisa e a implementação de outros instrumentos de gestão ambiental, visando o cumprimento da legislação e o atendimento das metas de controle e qualidade ambiental; </li>       
                <li class="mt-1">IV - 	Coordenar a elaboração de normas, padrões, critérios e procedimentos de licenciamento ambiental no âmbito do Município.</li>       
            </ul>
            
            <p class="mb-0"><b>À Coordenadoria de Proteção dos Recursos Naturais e Educação Ambiental</b>, compete:</p>
            <ul class="list-unstyled">
                <li class="mt-1">I - 	Planejar, coordenar, supervisionar e promover a execução de planos, programas e projetos relativos à educação ambiental, à arborização municipal, à gestão integrada da destinação de resíduos sólidos, à preservação e recuperação de bacias e micro bacias hidrográficos;</li>       
                <li class="mt-1">II - 	Desenvolver ações de preservação, proteção e conservação da biodiversidade, apoiando a realização de pesquisas; </li>       
                <li class="mt-1">III - 	Planejar, coordenar, supervisionar e implementar, nos termos da legislação pertinente, os processos de implantação, conservação e gestão das Unidades de Conservação.</li>       
            </ul>
            
